public class Record {
    String titleName;
    String tvRating;
    String genre;
    String titleId;
    String yearMade;
    String score;


    public Record() {
        this.titleId = titleId;
        this.tvRating = tvRating;
        this.titleName = titleName;
        this.genre = genre;
        this.yearMade = yearMade;
        this.score = score;
    }
}
